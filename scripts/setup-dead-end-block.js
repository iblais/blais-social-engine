console.log('Creating Dead End Block folder structure...\n');

let created = 0;
let errors = 0;

for (const dir of STRUCTURE) {
  const fullPath = path.join(BASE, dir.replace(/\//g, path.sep));
  try {
    fs.mkdirSync(fullPath, { recursive: true });
    console.log(`✓ Created: ${dir}`);
    created++;
  } catch (err) {
    console.error(`✗ Failed: ${dir} — ${err.message}`);
    errors++;
  }
}

console.log('\nCreating placeholder files...\n');

for (const [filePath, content] of Object.entries(PLACEHOLDERS)) {
  const fullPath = path.join(BASE, filePath.replace(/\//g, path.sep));
  try {
    if (!fs.existsSync(fullPath)) {
      fs.writeFileSync(fullPath, content, 'utf8');
      console.log(`✓ Created: ${filePath}`);
    } else {
      console.log(`- Skipped (exists): ${filePath}`);
    }
  } catch (err) {
    console.error(`✗ Failed: ${filePath} — ${err.message}`);
    errors++;
  }
}

console.log(`\n✅ Done — ${created} dirs, ${Object.keys(PLACEHOLDERS).length} files`);
if (errors > 0) console.log(`⚠️  ${errors} errors — check if G: drive is mounted`);

